import { type NextRequest, NextResponse } from "next/server"
import { supabase } from "@/lib/supabase"
import bcrypt from "bcryptjs"

export async function POST(request: NextRequest) {
  try {
    console.log("üîÑ Iniciando processo de registro...")

    const body = await request.json()
    console.log("üìù Dados recebidos:", {
      fullName: body.fullName,
      email: body.email,
      hasPassword: !!body.password,
    })

    const { fullName, email, password } = body

    // Valida√ß√µes
    if (!fullName || !email || !password) {
      console.log("‚ùå Dados obrigat√≥rios faltando")
      return NextResponse.json({ error: "Todos os campos s√£o obrigat√≥rios" }, { status: 400 })
    }

    if (password.length < 6) {
      console.log("‚ùå Senha muito curta")
      return NextResponse.json({ error: "Senha deve ter pelo menos 6 caracteres" }, { status: 400 })
    }

    // Verificar se email j√° existe
    console.log("üîç Verificando se email j√° existe...")
    const { data: existingUser, error: checkError } = await supabase
      .from("user_profiles")
      .select("id")
      .eq("email", email.trim().toLowerCase())
      .single()

    if (checkError && checkError.code !== "PGRST116") {
      console.error("‚ùå Erro ao verificar email:", checkError)
      return NextResponse.json({ error: "Erro interno do servidor" }, { status: 500 })
    }

    if (existingUser) {
      console.log("‚ùå Email j√° existe")
      return NextResponse.json({ error: "Este email j√° est√° cadastrado" }, { status: 400 })
    }

    // Hash da senha
    console.log("üîê Gerando hash da senha...")
    const hashedPassword = await bcrypt.hash(password, 12)

    // Buscar configura√ß√µes padr√£o
    console.log("‚öôÔ∏è Buscando configura√ß√µes padr√£o...")
    const { data: defaultLimitSetting } = await supabase
      .from("system_settings")
      .select("setting_value")
      .eq("setting_key", "max_agents_per_user")
      .single()

    const defaultLimit = defaultLimitSetting?.setting_value || 3

    // Criar usu√°rio
    console.log("üë§ Criando usu√°rio no banco...")
    const { data: newUser, error: userError } = await supabase
      .from("user_profiles")
      .insert([
        {
          full_name: fullName.trim(),
          email: email.trim().toLowerCase(),
          password_hash: hashedPassword,
          role: "user",
          status: "active",
          agents_limit: defaultLimit,
        },
      ])
      .select()
      .single()

    if (userError) {
      console.error("‚ùå Erro ao criar usu√°rio:", userError)
      return NextResponse.json({ error: "Erro ao criar usu√°rio: " + userError.message }, { status: 500 })
    }

    console.log("‚úÖ Usu√°rio criado:", newUser.id)

    // Criar configura√ß√µes do usu√°rio
    console.log("‚öôÔ∏è Criando configura√ß√µes do usu√°rio...")
    const { error: settingsError } = await supabase.from("user_agent_settings").insert([
      {
        user_id: newUser.id,
        agents_limit: defaultLimit,
      },
    ])

    if (settingsError) {
      console.error("‚ö†Ô∏è Erro ao criar configura√ß√µes (n√£o cr√≠tico):", settingsError)
    }

    console.log("üéâ Registro conclu√≠do com sucesso!")

    return NextResponse.json(
      {
        message: "Usu√°rio criado com sucesso",
        user: {
          id: newUser.id,
          email: newUser.email,
          full_name: newUser.full_name,
        },
      },
      { status: 201 },
    )
  } catch (error: any) {
    console.error("üí• Erro geral no registro:", error)
    return NextResponse.json({ error: "Erro interno do servidor: " + error.message }, { status: 500 })
  }
}
